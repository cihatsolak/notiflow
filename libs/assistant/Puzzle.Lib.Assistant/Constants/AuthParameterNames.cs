namespace Puzzle.Lib.Assistant.Constants;

/// <summary>
/// A class that contains parameter names used in authentication.
/// </summary>
public static class AuthParameterNames
{
    /// <summary>
    /// The parameter name for the access token.
    /// </summary>
    public const string AccessToken = "access_token";

    /// <summary>
    /// The parameter name for the refresh token.
    /// </summary>
    public const string RefreshToken = "refresh_token";

    /// <summary>
    /// The parameter name for the expiration time of the token.
    /// </summary>
    public const string ExpiresIn = "expires_in";
}
