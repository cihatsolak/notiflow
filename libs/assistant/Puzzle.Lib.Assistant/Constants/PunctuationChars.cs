namespace Puzzle.Lib.Assistant.Constants;

/// <summary>
/// This static class defines constant characters for various punctuation marks and symbols.
/// </summary>
public static class PunctuationChars
{
    /// <summary>
    /// Represents a white space character.
    /// </summary>
    public const char WhiteSpace = ' ';

    /// <summary>
    /// Represents an exclamation mark character.
    /// </summary>
    public const char ExclamationMark = '!';

    /// <summary>
    /// Represents a double quotation mark character.
    /// </summary>
    public const char DoubleQuotationMark = '"';

    /// <summary>
    /// Represents an apostrophe character.
    /// </summary>
    public const char Apostrophe = '\'';

    /// <summary>
    /// Represents a comma character.
    /// </summary>
    public const char Comma = ',';

    /// <summary>
    /// Represents a hyphen character.
    /// </summary>
    public const char Hyphen = '-';

    /// <summary>
    /// Represents a dot (period) character.
    /// </summary>
    public const char Dot = '.';

    /// <summary>
    /// Represents a colon character.
    /// </summary>
    public const char Colon = ':';

    /// <summary>
    /// Represents a semicolon character.
    /// </summary>
    public const char Semicolon = ';';

    /// <summary>
    /// Represents a question mark character.
    /// </summary>
    public const char QuestionMark = '?';

    /// <summary>
    /// Represents a left square bracket character.
    /// </summary>
    public const char LeftSquareBracket = '[';

    /// <summary>
    /// Represents a right square bracket character.
    /// </summary>
    public const char RightSquareBracket = ']';

    /// <summary>
    /// Represents an underscore character.
    /// </summary>
    public const char Underscore = '_';

    /// <summary>
    /// Represents a left curly bracket character.
    /// </summary>
    public const char LeftCurlyBracket = '{';

    /// <summary>
    /// Represents a right curly bracket character.
    /// </summary>
    public const char RightCurlyBracket = '}';

    /// <summary>
    /// Represents a left round bracket character.
    /// </summary>
    public const char LeftRoundBracket = '(';

    /// <summary>
    /// Represents a right round bracket character.
    /// </summary>
    public const char RightRoundBracket = ')';
}
